class CustomUNet(nn.Module):
    def __init__(self, baseUNet, n_channels, n_classes, bilinear=True):
        super(CustomUNet, self).__init__()
        self.n_channels = n_channels
        self.n_classes = n_classes
        self.bilinear = bilinear
        
        self.inc = DoubleConv(n_channels, 64)
        self.down1 = Down(64, 128)
        self.pool1 = nn.MaxPool2d(2)
        self.conv1x1 = nn.Conv2d(128, 6, kernel_size=1, bias=False)
        self.bn1 = nn.BatchNorm2d(6)
        #self.orig_net = list(baseUNet.children())[0]
        self.baseUNet = baseUNet #nn.Sequential(*self.orig_net)
        self.finalup = nn.Upsample(scale_factor=4, mode='bilinear', align_corners=True)
        #print(self.baseUNet)
        #self.up_128 = 
        
    def forward(self, x):
        out = self.inc(x)
        out = self.pool1(self.down1(out))
        
        out = F.relu(self.bn1(self.conv1x1(out)))
        out = self.baseUNet(out)
        out = self.finalup(out)
        return out

